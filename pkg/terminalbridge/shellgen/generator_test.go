// =============================================================================
// TestGenerate — table-driven tests
// =============================================================================

func TestGenerate(t *testing.T) {
	boolPtr := func(b bool) *bool { return &b }

	tests := []struct {
		name           string
		config         ShellConfig
		mockFile       func(string) ([]byte, error)
		wantContains   []string
		wantNotContain []string
	}{
		{
			name:     "empty config produces header and footer",
			config:   ShellConfig{},
			mockFile: mockReadFile("", os.ErrNotExist),
			wantContains: []string{
				"#!/usr/bin/env zsh",
				"generated by DevOpsMaestro",
				"End of .zshrc.workspace",
			},
		},
		{
			name: "host zshrc included when present",
			config: ShellConfig{
				HostZshrcPath: "/fake/.zshrc",
			},
			mockFile: mockReadFile("# My host config\nexport EDITOR=vim\n", nil),
			wantContains: []string{
				"Host .zshrc",
				"# My host config",
				"export EDITOR=vim",
			},
		},
		{
			name: "host zshrc missing gracefully handled",
			config: ShellConfig{
				HostZshrcPath: "/nonexistent/.zshrc",
			},
			mockFile: mockReadFile("", os.ErrNotExist),
			wantContains: []string{
				"Host .zshrc not found",
			},
			wantNotContain: []string{
				"export EDITOR",
			},
		},
		{
			name: "host zshrc skipped when disabled",
			config: ShellConfig{
				HostZshrcPath:    "/fake/.zshrc",
				IncludeHostZshrc: boolPtr(false),
			},
			mockFile: mockReadFile("# Should not appear\n", nil),
			wantNotContain: []string{
				"Host .zshrc",
				"Should not appear",
			},
		},
		{
			name: "env vars injected and sorted",
			config: ShellConfig{
				EnvVars: map[string]string{
					"WORKSPACE": "/home/dev/project",
					"APP_ENV":   "development",
				},
			},
			mockFile: mockReadFile("", os.ErrNotExist),
			wantContains: []string{
				"Workspace Environment Variables",
				`export APP_ENV="development"`,
				`export WORKSPACE="/home/dev/project"`,
			},
		},
		{
			name: "prompt config appended",
			config: ShellConfig{
				PromptConfig: `eval "$(starship init zsh)"`,
			},
			mockFile: mockReadFile("", os.ErrNotExist),
			wantContains: []string{
				"Prompt Configuration",
				`eval "$(starship init zsh)"`,
			},
		},
		{
			name: "workspace name in header",
			config: ShellConfig{
				WorkspaceName: "my-project",
			},
			mockFile: mockReadFile("", os.ErrNotExist),
			wantContains: []string{
				"Workspace: my-project",
			},
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			g := New()
			g.readFile = tt.mockFile

			output, err := g.Generate(tt.config)
			if err != nil {
				t.Fatalf("unexpected error: %v", err)
			}

			for _, want := range tt.wantContains {
				if !strings.Contains(output, want) {
					t.Errorf("output should contain %q\nGot:\n%s", want, output)
				}
			}
			for _, notWant := range tt.wantNotContain {
				if strings.Contains(output, notWant) {
					t.Errorf("output should NOT contain %q\nGot:\n%s", notWant, output)
				}
			}
		})
	}
}

// =============================================================================
// TestGenerate_WithPlugins
// =============================================================================

func TestGenerate_WithPlugins(t *testing.T) {
	g := New()
	g.readFile = mockReadFile("", os.ErrNotExist)

	config := ShellConfig{
		Plugins: []*plugin.Plugin{
			{
				Name:     "zsh-autosuggestions",
				Repo:     "zsh-users/zsh-autosuggestions",
				Manager:  plugin.PluginManagerManual,
				Priority: 10,
				Enabled:  true,
			},
			{
				Name:     "zsh-syntax-highlighting",
				Repo:     "zsh-users/zsh-syntax-highlighting",
				Manager:  plugin.PluginManagerManual,
				Priority: 20,
				Enabled:  true,
			},
		},
	}

	output, err := g.Generate(config)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	// Plugins should appear
	if !strings.Contains(output, "zsh-autosuggestions") {
		t.Error("output should contain zsh-autosuggestions")
	}
	if !strings.Contains(output, "zsh-syntax-highlighting") {
		t.Error("output should contain zsh-syntax-highlighting")
	}

	// Verify load order — autosuggestions (priority 10) before highlighting (priority 20)
	sugIdx := strings.Index(output, "zsh-autosuggestions")
	hlIdx := strings.Index(output, "zsh-syntax-highlighting")
	if sugIdx > hlIdx {
		t.Error("zsh-autosuggestions should appear before zsh-syntax-highlighting (load order)")
	}
}

