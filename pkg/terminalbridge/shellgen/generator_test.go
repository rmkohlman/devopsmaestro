// =============================================================================
// TestGenerate_FullComposite
// =============================================================================

func TestGenerate_FullComposite(t *testing.T) {
	g := New()
	g.readFile = mockReadFile("# Host zshrc content\n", nil)

	config := ShellConfig{
		HostZshrcPath: "/home/user/.zshrc",
		WorkspaceName: "test-workspace",
		EnvVars: map[string]string{
			"PROJECT_ROOT": "/workspace",
		},
		Plugins: []*plugin.Plugin{
			{
				Name:    "zsh-completions",
				Repo:    "zsh-users/zsh-completions",
				Manager: plugin.PluginManagerManual,
				Enabled: true,
			},
		},
		ShellConfigs: []*shell.Shell{
			{
				Name:      "basics",
				ShellType: shell.ShellTypeZsh,
				Enabled:   true,
				Env: []shell.EnvVar{
					{Name: "LANG", Value: "en_US.UTF-8"},
				},
			},
		},
		PromptConfig: `eval "$(starship init zsh)"`,
	}

	output, err := g.Generate(config)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	// Verify all sections present and in correct order
	sections := []string{
		"Host .zshrc",
		"Host zshrc content",
		"Workspace Environment Variables",
		"PROJECT_ROOT",
		"LANG",
		"zsh-completions",
		"Prompt Configuration",
		"starship init zsh",
		"End of .zshrc.workspace",
	}

	lastIdx := -1
	for _, section := range sections {
		idx := strings.Index(output, section)
		if idx == -1 {
			t.Errorf("output missing section: %q", section)
			continue
		}
		if idx <= lastIdx {
			t.Errorf("section %q should appear after previous section", section)
		}
		lastIdx = idx
	}
}

// =============================================================================
// TestShellConfig_ShouldIncludeHostZshrc
// =============================================================================

func TestShellConfig_ShouldIncludeHostZshrc(t *testing.T) {
	boolPtr := func(b bool) *bool { return &b }

	tests := []struct {
		name     string
		config   ShellConfig
		expected bool
	}{
		{"nil defaults to true", ShellConfig{}, true},
		{"explicit true", ShellConfig{IncludeHostZshrc: boolPtr(true)}, true},
		{"explicit false", ShellConfig{IncludeHostZshrc: boolPtr(false)}, false},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got := tt.config.ShouldIncludeHostZshrc()
			if got != tt.expected {
				t.Errorf("ShouldIncludeHostZshrc() = %v, want %v", got, tt.expected)
			}
		})
	}
}
