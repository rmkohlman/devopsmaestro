// =============================================================================
// Helper: mock readFile for testing
// =============================================================================

func mockReadFile(content string, err error) func(string) ([]byte, error) {
	return func(_ string) ([]byte, error) {
		if err != nil {
			return nil, err
		}
		return []byte(content), nil
	}
}

